package de.hsmainz.gi.indoornavcl.util;

import android.content.Context;
import android.util.Log;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.TextView;
import de.hsmainz.gi.indoornavcl.R;
import de.hsmainz.gi.indoornavcl.comm.types.Beacon;
import de.hsmainz.gi.indoornavcl.comm.types.WkbLocation;
import de.hsmainz.gi.indoornavcl.positioning.TinyCoordinate;

/**
 * @author Jan 'KekS' M. <a href='mailto:keks@keksfabrik.eu'>mail</a>, 19.02.15.
 */
public class    BeaconLocationRowAdapter
    extends     BaseAdapter {

    private Context         context;
    private WkbLocation[]   data;
    private LayoutInflater  inflater = null;

    public BeaconLocationRowAdapter(Context context, WkbLocation[] data) {
        Log.d("BEACONLOCATION ROW", "ctor called with beacon-count: " + data.length);
        this.context = context;
        this.data = data;
        inflater = (LayoutInflater) context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
    }
    /**
     * How many items are in the data set represented by this Adapter.
     *
     * @return Count of items.
     */
    @Override
    public int getCount() {
        return data.length;
    }

    /**
     * Get the data item associated with the specified position in the data set.
     *
     * @param position Position of the item whose data we want within the adapter's
     *                 data set.
     * @return The data at the specified position.
     */
    @Override
    public WkbLocation getItem(int position) {
        return data[position];
    }

    /**
     * Get the row id associated with the specified position in the list.
     *
     * @param position The position of the item within the adapter's data set whose row id we want.
     * @return The id of the item at the specified position.
     */
    @Override
    public long getItemId(int position) {
        return position;
    }

    /**
     * Get a View that displays the data at the specified position in the data set. You can either
     * create a View manually or inflate it from an XML layout file. When the View is inflated, the
     * parent View (GridView, ListView...) will apply default layout parameters unless you use
     * {@link android.view.LayoutInflater#inflate(int, android.view.ViewGroup, boolean)}
     * to specify a root view and to prevent attachment to the root.
     *
     * @param position    The position of the item within the adapter's data set of the item whose view
     *                    we want.
     * @param convertView The old view to reuse, if possible. Note: You should check that this view
     *                    is non-null and of an appropriate type before using. If it is not possible to convert
     *                    this view to display the correct data, this method can create a new view.
     *                    Heterogeneous lists can specify their number of view types, so that this View is
     *                    always of the right type (see {@link #getViewTypeCount()} and
     *                    {@link #getItemViewType(int)}).
     * @param parent      The parent that this view will eventually be attached to
     * @return A View corresponding to the data at the specified position.
     */
    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        View vi = convertView;
        if (vi == null)
            vi = inflater.inflate(R.layout.beacon_location_row, null);
        WkbLocation loc = data[position];
        Beacon b = loc.getBeacon();
        TinyCoordinate p = loc.getCoord().getCoordinate();
        p.round(3);
        Log.d("BEACONLOCATION ROW", "INFLATING for " + position + ": " + StringUtils.toString(b) + " @ " + StringUtils.toString(p));
        ((TextView) vi.findViewById(R.id.beacon_uuid_text)).setText(b.getUuid());
        ((TextView) vi.findViewById(R.id.beacon_major_text)).setText(Integer.toString(b.getMajor()));
        ((TextView) vi.findViewById(R.id.beacon_minor_text)).setText(Integer.toString(b.getMinor()));
        ((TextView) vi.findViewById(R.id.txtX)).setText(Double.toString(p.x));
        ((TextView) vi.findViewById(R.id.txtY)).setText(Double.toString(p.y));
        ((TextView) vi.findViewById(R.id.txtZ)).setText(Double.toString(p.z));
        return vi;
    }
}